
// TEMPLATE-1

var template1 = document.querySelector(".template__1");
var templateInfo1 = document.querySelector(".template__1 .template__info");
var templatePrice1 = document.querySelector(".template__1 .template__price");
var templateLook1 = document.querySelector(".template__1 .template__look");


template1.addEventListener("mouseover", function () {
  templateInfo1.classList.add("template__info--show");
});

templateInfo1.addEventListener("mouseover", function () {
  templateInfo1.classList.add("template__info--show");
});

template1.addEventListener("mouseleave", function () {
  setTimeout(function() {
    templateInfo1.classList.remove("template__info--show");
  }, 400);
});

templatePrice1.addEventListener("mouseenter", function () {
  setTimeout(function() {
    templatePrice1.classList.toggle("visually-hidden");
    templateLook1.classList.toggle("visually-hidden");
  }, 500);
});

templateLook1.addEventListener("mouseout", function () {
  setTimeout(function() {
    templatePrice1.classList.toggle("visually-hidden");
    templateLook1.classList.toggle("visually-hidden");
  }, 500);
});


